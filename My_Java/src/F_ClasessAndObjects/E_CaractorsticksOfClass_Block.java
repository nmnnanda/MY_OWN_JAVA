package F_ClasessAndObjects;

public class E_CaractorsticksOfClass_Block {
		   public static void main(String[] args) {
		      {		      // This is the start of the block
		         int a = 5;
		         int b = 7;
		         int c = a + b;
		         System.out.println("The sum of " + a + " and " + b + " is " + c);
		      } // This is the end of the block
		      
		      int a = 10;		      // This is outside of the block
		      int b = 3;
		      int c = a / b;
		      System.out.println("The result of " + a + " divided by " + b + " is " + c);
		   }
		}
