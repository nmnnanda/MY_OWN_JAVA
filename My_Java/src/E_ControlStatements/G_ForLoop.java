package E_ControlStatements;

public class G_ForLoop {

	public static void main(String[] args) {
		System.out.println("Skillking IoT Student ");
		for (int i = 1; i <= 10; i++)
		{
		System.out.print(i+" ");
		}
//-------------------------------------------------------------				
	    int[] numbers = {1, 2, 3, 4, 5};   // Define an array of integers	  	    
	    for (int i = 0; i < numbers.length; i++) {  // Iterate over the array using a for loop	      
	      System.out.println(numbers[i]); // Print the current element of the array
	    }
}
}